<?php

class Calculator
{
    public static function add($x1, $x2)
    {
        return $x1 + $x2;
    }

    public static function subtract($x1, $x2)
    {
        return $x1 - $x2;
    }

    public static function multiply($x1, $x2)
    {
        return $x1 * $x2;
    }

    public static function divide($x1, $x2)
    {
        return $x1 / $x2;
    }
}
